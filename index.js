const app = Vue.createApp({
    data() {
        return {
            gateShow: true,
            homeShow: false,
            navItem: [
                {
                    main: '书法',
                    sub: ['篆书', '隶书', '楷书', '行书', '草书']
                },
                {
                    main: '中国画',
                    sub: ['人物画', '花鸟画', '山水画']
                },
                {
                    main: '诗词曲赋',
                    sub: ['汉赋', '唐诗', '宋词', '元曲']
                },
                { main: '传统节日', sub: [] }
                // '书法', '中国画', '诗词曲赋', '传统节日'
            ],
            curItem: 0,
            curSubItem: 0
        }
    },
    components: {
        gate,
        // home,
    },
    computed: {
        navImgPath() {
            return function (item) {
                return `./img/${item}.png`
            }
        },
        routePath() {
            switch (this.curItem) {
                case 0: return '/handwrite';
                case 1: return '/paint';
                case 2: return '/poetry';
                default: return '/festival';
            }
        },
        firstPath() {
            const arr = this.$route.path.split('/');
            // console.log(arr);
            const res = '/' + arr[1];
            // console.log(res);
            return res;
        },
        subPath() {
            // console.log(this.$route.meta);
            const arr = this.$route.path.split('/');
            // console.log(arr);
            const res = arr[2];
            // console.log(res);
            return res;
        },
        fullPath() {
            return this.$route.path;
        }
    },
    methods: {
        navClick(index) {
            if (index !== this.curItem) {
                this.curItem = index;
                this.curSubItem = 0;
                this.$router.push(this.routePath);
                const a = document.querySelector('article');
                a.scrollTop = 0;
            }
        },
        subClick(index) {
            let path = this.$route.meta[0] + '/' + this.$route.meta[index + 1];
            // console.log(path);
            this.curSubItem = index;
            this.$router.push(path);
            const a = document.querySelector('article');
            a.scrollTop = 0;
        }
    },
    watch: {
        firstPath(val) {
            switch (val) {
                case '/handwrite': this.curItem = 0; break;
                case '/paint': this.curItem = 1; break;
                case '/poetry': this.curItem = 2; break;
                default: this.curItem = 3;
            }
        },
        subPath(val) {
            // console.log(val);
            for (let key in this.$route.meta) {
                if (this.$route.meta[key] === val) {
                    this.curSubItem = key - 1;
                    // console.log(typeof this.curSubItem, this.curSubItem);
                    break;
                }
            }
        },
        fullPath(val) {
            // console.log(val);
            if (val !== '/handwrite' && this.gateShow && !this.homeShow) {
                this.gateShow = false;
                this.homeShow = true;
            }
        }
    }
})

app.use(router)

app.mount('#app')