        split() {
            return function (s) {
                const arr = [];
                let left = 0;
                for (let i = 0; i < s.length; i++) {
                    if (s[i] === '，' || s[i] === '。' || s[i] === '？' ||
                        s[i] === '！' || s[i] === '：' || s[i] === '。' ||
                        s[i] === '；') {
                        arr.push(s.slice(left, i + 1));
                        left = i + 1;
                    }
                }
                return arr;
            }
        }
